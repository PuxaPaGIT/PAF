function mostrarFormLogin() { 
    document.querySelector(".form-login").style.display = "block"; /* aparece */
    document.querySelector(".form-registar").style.display = "none"; /* nao aparece */
}

function mostrarFormRegistar() { 
    document.querySelector(".form-login").style.display = "none";/* nao aparece */
    document.querySelector(".form-registar").style.display = "block"; /* aparece */
}

